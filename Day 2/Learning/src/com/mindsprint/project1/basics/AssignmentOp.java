package com.mindsprint.project1.basics;

public class AssignmentOp {
    public static void main(String[] args) {
        int num1 = 14; // simple assigment
        num1 += 10;
        System.out.println("+ 10 : " + num1);
        num1 -= 7;
        System.out.println("- 7 : " + num1);
        num1 *= 7;
        System.out.println("* 7 : " + num1);
        num1 /= 7;
        System.out.println("/ 7 : " + num1);
        num1 %= 7;
        System.out.println("% 7 : " + num1);
    }
}
